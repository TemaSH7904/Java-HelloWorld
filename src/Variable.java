public class Variable {
    public static void main(String[] args) {
        int jhon = 100;
        int nick = jhon * 5;
        int summ = jhon + nick;
        System.out.println(summ);

        int a = 10;
        int b = 3;
        int c = a / b;
        System.out.println(c+" получили целое число");

        int d = 10;
        int e = 3;
        int f = d % e;
        System.out.println(f+" остаток от деления");
    }
}
